    private void btnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayActionPerformed
        FileInputStream fis = null; // File input stream equals nothing
        fc = new JFileChooser(dir); //create a new file chooser with a file variable
        int option = fc.showOpenDialog(this); // open a dialog
        if (option == JFileChooser.APPROVE_OPTION) { // show dialog with the ok option
            file = fc.getSelectedFile(); //get the file 
            dir = fc.getCurrentDirectory(); //goes to the current directory
            try {
                String str = ""; //empty string
                fis = new FileInputStream(file); //create a new file input stream writing to file
                int len = (int) file.length(); // int variable equal to the length of file 
                for (int i = 0; i < len; i++) {
                    char str2 = 0;
                    try {
                        str2 = (char) fis.read();
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                    str = str + str2;
                }
                lblMessage.setText(str); // Text area shows the result formatted
            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    if (fis != null) { //if file input stream is empty
                        fis.close(); // close the stream
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnDisplayActionPerformed

