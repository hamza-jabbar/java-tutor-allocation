    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        getRecord(); //gets all the records from all the fields filled in
        fc = new JFileChooser(dir); // create a new filechooser

        // Student table model
        DefaultTableModel sModel = (DefaultTableModel) jTableStudent.getModel();

        // Tutor table model
        DefaultTableModel tModel = (DefaultTableModel) jTableTutor.getModel();

        FileNameExtensionFilter filter;
        filter = new FileNameExtensionFilter("*.txt", new String[]{"txt"});
        fc.addChoosableFileFilter(filter); // when saving the data, an option for saving a file is shown
        int option = fc.showSaveDialog(this); //shows a dialog box
        if (option == JFileChooser.APPROVE_OPTION) { // if the user clicks the OK button on the dialog box
            dir = fc.getCurrentDirectory(); // File object is equal to the filechooser

            // This is adding rows to the student table
            sModel.addRow(new Object[]{txtUobNumber.getText(), txtSFirstname.getText(),
                txtSSurname.getText(), txtSEmail.getText(), txtCourse.getText(), txtCourseType.getText(),
                txtCourseDuration.getText(), txtStartDate.getText(), txtEndDate.getText()});
            
                // This is adding rows to the tutor table
                tModel.addRow(new Object[]{txtID.getText(), txtTitle.getText(), txtTFirstname.getText(),
                    txtTSurname.getText(), txtTEmail.getText()});
                file = fc.getSelectedFile(); // can choose a file
            
            try {
                write(); // writes the information from the tables to a file in a certain format
                lblMessage.setText("File saved successfully");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

