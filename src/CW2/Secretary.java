    private void write() throws IOException {
        BufferedWriter bw = null; //empty buffered writer

        // Student
        String uobnumber = txtUobNumber.getText().toString();
        String sFirstname = txtSFirstname.getText().toString();
        String sSurname = txtSSurname.getText().toString();

        // Tutor
        String id = txtID.getText().toString();
        String title = txtTitle.getText().toString();
        String tFirstname = txtTFirstname.getText().toString();
        String tSurname = txtTSurname.getText().toString();
        try {
            // a character output stream with a file output stream that gets the extention
            bw = new BufferedWriter(new FileWriter(file + getExtention(), true));
            //writes the data given in the format given

            bw.write("Student " + uobnumber + " is added to " + title + " " + tFirstname + " " + tSurname + "'s tutor group. " + "\n");

            // Shows this message when the data is written
            JOptionPane.showMessageDialog(rootPane, "Data Saved to file");

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (bw != null) { //if the buffered writer not empty
                bw.close(); //close the buffered writer
            }
        }
    }

    private String getExtention() {
        String ext = "";
        //the file choose gets the selected file and the decription of the file
        String extension = fc.getFileFilter().getDescription();
        if (extension.equals("*.txt")) {
            ext = ".txt";
        }
        return ext;
    }

